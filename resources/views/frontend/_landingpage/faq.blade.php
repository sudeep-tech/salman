                      <h2><a name="jn_gen_q"></a>Using afnaideal - General</h2>

                      <ol>

                        <li class="question">

                          <div class="q"><a name="jn_gen_q01"></a>What should I do when I don't get the User ID and Password even after registering or signing up in afnaideal.com?</div>

                          <div>

                              afnaideal.com sends an e-mail that contains the User ID and Password to the email address that you have provided while registering/signing up. If you have by mistake provided a wrong email address (usually a spelling mistake) then it bounces back to us. In that case we will try to contact you via your telephone number, provided you entered a phone number in the sign-up page while registering. If the email address is not your own the mail goes to that person and you must get the information from them. In that case, you can send a request email to info@afnaideal.com that would include your User ID, Full Name, your correct email address (it is advised to send the mail from the email address that you would provide), and Telephone No, date of sign up. We will verify the information, make the necessary changes and inform you.

                          </div>

                        </li>



                        <li class="question">

                          <div class="q"><a name="jn_gen_q02"></a>Can afnaideal.com remember my User ID and Password?</div>

                          <div>

                              Yes, afnaideal.com remembers your User ID and Password. Next time you login at afnaideal.com, check the Remember Me check box on the "User Login". Once you have done this, you do not have to enter the User ID and Password in the future. We recommend this feature only if the computer that you are using is personal and secure. If you are accessing the site from public place like cyber caf√© or a library, then we recommend that you do not use this feature.

                          </div>

                        </li>



                        <li class="question">

                          <div class="q"><a name="jn_gen_q03"></a>What do I do if I forget my Password and/or User ID?</div>

                          <div>

                              If you have accidentally forgotten the Password and/or User ID and misplaced/deleted the email that you have received (with your User ID and Password after Sign Up) then log on to http://www.afnaideal.com. On the main page, under "User Login" section, click on to "forgot my password". A small pop-up window comes up where you will have to enter the e-mail address that you have used while registering at afnaideal.com. Once you are done, click Submit and your User ID and Password would be send to your email address.

                          </div>

                        </li>



                        <li class="question">

                          <div class="q"><a name="jn_gen_q04"></a>How do I change my Password?</div>

                          <div>

                              Login by entering your User ID and Password at afnaideal.com. Once you are logged in, you would see a set of menu items on your left-hand side of the screen. Click "Change Password" under the "Admin" section. In the next screen, enter your old password and then new password, twice. Press "Change Password". You will be logged out from afnaideal.com and will have to log in again using your new password.

                          </div>

                        </li>



                         <li class="question">

                          <div class="q"><a name="jn_gen_q05"></a>How do I change my email address?</div>

                          <div>

                                Login by entering your User ID and Password at afnaideal.com. Once you are logged in, you would see a set of menu items on your left-hand side of the screen. Click "Change Email" under the "Admin" section. In the next screen, enter your new email address, and press "Change Email".. A new password will be sent to you at the new e-mail address that you have provided. You will be logged out from afnaideal.com. You will then have to login back to afnaideal with the new password that is provided.

                          </div>

