                      <h2><a name="jn_jseek_q"></a>Using afnaideal - Job Seeker / Resume Holder</h2>

                      <ol>

                        <li class="question">

                          <div class="q"><a name="jn_jseek_q01"></a>How do I register as a Job Seeker / Resume Holder?</div>

                          <div>

                                If you are a Job Seeker and want to sign up, log on to http://www.afnaideal.com main page, on your left-hand side under the "Register" section, click on "Job Seeker Sign-Up" button. On the next page, fill up the form and your User ID and Password will be send to the e-mail address that you provide during Sign Up. 

                                PS: If you do not have your personal e-mail address, you can get one for free from free email service provider such as Hotmail, Yahoo, etc.

                          </div>

                        </li>



                        <li class="question">

                          <div class="q"><a name="jn_jseek_q02"></a>How do I add/submit my resume/CV/Bio-data?</div>

                          <div>

                                Once you have logged in giving the User ID and Password under "Users Login" section in the main page of afnaideal.com, you would be able to see a set of menus on the left-hand side of the page. Under the "my Resume" section, please click the "Add New Resume". Now you enter the information for this resume. You can submit as many resumes as you want with different titles for different job descriptions. If you think that the space provided in the form is not enough then you can upload a word file to supplement the resume. Any company interested in your resume can view the document that you have submitted along with your resume.

                          </div>

                        </li>



                        <li class="question">

                          <div class="q"><a name="jn_jseek_q03"></a>Does afnaideal.com inform me if there is a vacancy that matches my resume/CV?</div>

                          <div>

                                Yes! We know that our users have limited time, especially to surf the net. If you want an email notification whenever a job that matches your criteria is posted, then please set your criteria as follows. After logging in, click on the "my Resume Home" under "my Resume" section that you see on the menu list on the left-hand side of the screen. Press the "Edit Subscription Detail" button on the "My Resume Home" page. Here, you can see a set of choices regarding you preferences - select the ones that apply, check the E-mail Notification box and save the settings by clicking the "Set my Jobs Preference". Next time if there is any vacancy that matches your setting then you will receive an e-mail with the URL of the job posted at afnaideal.com. By clicking on the URL or copying and pasting the URL to the browser, you can directly reach the detail information of the job.

                          </div>

                        </li>



                        <li class="question">

                          <div class="q"><a name="jn_jseek_q04"></a>Can afnaideal team post my resume/CV for me?</div>

                          <div>

                                Yes! We can help you post your resume for you. But we strongly suggest that members post their resumes themselves. If you are a user that does not have Internet access or very limited access to the Internet then we can help you; but before that you would have to register at afnaideal.com yourself. Once you have registered, send us your resume/CV in readable Word Format or text format with your User ID, email address (that you have used while registering) via e-mail (at info@afnaideal.com ) or mail us at G.P.O. Box 8975 E.P.C. 1011, Kathmandu, Nepal.

                          </div>

                        </li>



                         <li class="question">

                          <div class="q"><a name="jn_jseek_q05"></a>How do I know that the resume/CV that I posted is registered in the database?</div>

                          <div>

                              Once you have submitted your resume, you will see a page confirming that you have successfully submitted a resume. If you want to confirm your submission further then you can click "my Resume Home" under "my Resume" section where you will see your resume title listed.

                          </div>

                        </li>



                        <li class="question">

                          <div class="q"><a name="jn_jseek_q06"></a>Can I apply online to a vacancy posting?</div>

                          <div>

                                Definitely! We give the user the facility to apply to any open post online, but this depends also upon whether the employer have given the facility to the user to post the resume either Online, Send Email or By Post. You will have to use the facility provided by the employer and you need to post at least one resume at afnaideal.com to apply online.

                          </div>

                        </li>



                         <li class="question">

                          <div class="q"><a name="jn_jseek_q07"></a>Can I send my resume/CV to the employer online?</div>

                          <div>

                                If the employer has given the facility to post the resume Online then you can definitely send your resume online in reply to any job posting/vacancy.

                          </div>

                        </li>



                        <li class="question">

                          <div class="q"><a name="jn_jseek_q08"></a>How can I search for a job of my criteria/choice?</div>

                          <div>

                                On the Left-hand side of the main page of afnaideal.com, you will be able to see Jobs Search area. Place a keyword or keywords that matches your job criteria in the text box and click Go. If you want to search for a specific job you may want to use the detailed Jobs Search screen. You can do this by clicking at "detail Jobs search". Else if you are a registered user then log in at afnaideal.com with your User ID and Password. Click "Search Job" under "Search" that you would see in the menu items on your left-hand of the screen. In the next screen where you can fill in the details of the type of job that best matches your criteria. Once you are finished with the form, click the Go Search button and afnaideal will list you all the titles of all the job postings that match your criteria. For detailed information on the listed jobs, click on the Title. According to the facility given by the Employer, you can apply to the job either Online, Send Email or By Post.

                          </div>

                        </li>



